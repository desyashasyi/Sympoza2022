        Silver Medal KoPSI 2021 Kategori FTR (Fisika Terapan dan Rekayasa). Selama masa pendidikannya, ia memperoleh Kartu 
        Jakarta Pintar (KJP) yang merupakan bantuan pemerintah bagi siswa kurang mampu. Rifky memanfaatkannya untuk membeli 
        peralatan sekolah seperti seragam, buku, dan lain-lain. Hal ini memperingan beban ayahnya yang harus mengalokasikan 
        pendapatan untuk uang pendidikan Rifky serta kebutuhan sehari-hari.
