    <h4>REPUBLIKA.CO.ID, JAKARTA -- Menteri Koordinator Bidang Kemaritiman dan Investasi Luhut Binsar Pandjaitan menjamin pasokan dan harga minyak goreng di pasaran dengan penyempurnaan kebijakan Domestic Market Obligation (DMO) dan Domestic Price Obligation (DPO).<h4>
<div>
<div>
    <h4>Oleh karena itu, ia pun mengimbau masyarakat tidak panik, galau, dan khawatir pasokan domestik akan berkurang atau harga akan kembali meningkat. "Selain menjalankan pembukaan keran ekspor, pemerintah juga secara resmi mengubah kebijakan minyak goreng curah yang tadinya berbasis subsidi menjadi berbasis pemenuhan kewajiban pasar domestik (DMO) dan kewajiban harga domestik (DPO)," kata Luhut dalam konferensi pers daring di Jakarta, Ahad (5/6/2022).<h4>
<div>
<div>
    <h4>Menurut Luhut, langkah tersebut dilakukan untuk memastikan ketersediaan minyak goreng domestik di harga yang terjangkau selepas pelarangan ekspor CPO dan bahan baku minyak goreng dicabut. "Dengan kebijakan ini, pemerintah mengimbau masyarakat untuk tidak panik, atau tidak perlu galau atau khawatir pasokan domestik akan berkurang atau harga akan kembali meningkat. Ini kami pastikan tidak terjadi," tegasnya.<h4>
<div>
<div>
    <h4>Mantan Menko Polhukam itu pun menjelaskan, jumlah DMO yang ditetapkan pemerintah sejak 1 Juni 2022 sebesar 300 ribu ton minyak goreng per bulan. Jumlah itu lebih tinggi 50 persen dibandingkan kebutuhan domestik.
        "Hal ini dilakukan untuk membanjiri pasar domestik hingga dapat memudahkan masyarakat dalam mencari minyak goreng curah dengan harga eceran tertinggi Rp14 ribu hingga Rp 15 ribu sekian," katanya.<h4>
<div>
<div>
    <h4>Pemerintah, lanjut Luhut, juga akan menyalurkan minyak goreng curah dengan harga eceran tertinggi itu ke daerah-daerah yang sebelumnya tidak terjangkau oleh program ini dengan baik. Pemerintah juga akan memberikan kompensasi penambahan biaya angkut agar program minyak goreng curah untuk rakyat itu bisa dijangkau di seluruh pelosok Tanah Air.<h4>
<div>
<div>
    <h4>Luhut menerangkan alokasi DMO akan dibagi tidak hanya berdasarkan kapasitas produksi tapi juga kepatuhan terhadap pemenuhannya. Mereka yang patuh akan lebih cepat untuk bisa melakukan ekspor daripada mereka yang tidak patuh dalam memenuhi DMO.<h4>
<div>
<div>
    <h4>"Jadi kalau dia tidak memenuhi DMO-nya, dia juga tidak akan mendapatkan fasilitas ekspornya," katanya.<h4>
<div>
<div>
    <h4>Sementara itu, terkait kewajiban harga domestik (DPO), pemerintah tidak hanya menerapkannya terhadap produsen CPO dan minyak goreng tapi juga hingga tingkat distributor. Penentuan harga DPO ini akan menjadi dasar pengawasan dan penindakan oleh satgas di lapangan yang terdiri dari berbagai unsur mulai dari Polri, TNI, Kejaksaan hingga pemda terkait.<h4>
